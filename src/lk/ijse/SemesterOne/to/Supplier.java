package lk.ijse.SemesterOne.to;

public class Supplier {
    private String sup_id;
    private String sup_name;
    private String sup_address;
    private String sup_email;
    private String sup_contact;

    public Supplier(String sup_id, String sup_name, String sup_address, String sup_email, String sup_contact) {
        this.sup_id = sup_id;
        this.sup_name = sup_name;
        this.sup_address = sup_address;
        this.sup_email = sup_email;
        this.sup_contact = sup_contact;
    }

    public Supplier(String sup_name, String sup_address, String sup_email, String sup_contact) {
    }

    public String getSup_id() {
        return sup_id;
    }

    public void setSup_id(String sup_id) {
        this.sup_id = sup_id;
    }

    public String getSup_name() {
        return sup_name;
    }

    public void setSup_name(String sup_name) {
        this.sup_name = sup_name;
    }

    public String getSup_address() {
        return sup_address;
    }

    public void setSup_address(String sup_address) {
        this.sup_address = sup_address;
    }

    public String getSup_email() {
        return sup_email;
    }

    public void setSup_email(String sup_email) {
        this.sup_email = sup_email;
    }

    public String getSup_contact() {
        return sup_contact;
    }

    public void setSup_contact(String sup_contact) {
        this.sup_contact = sup_contact;
    }

    @Override
    public String toString() {
        return "Supplier{" +
                "sup_id='" + sup_id + '\'' +
                ", sup_name='" + sup_name + '\'' +
                ", sup_address='" + sup_address + '\'' +
                ", sup_email='" + sup_email + '\'' +
                ", sup_contact='" + sup_contact + '\'' +
                '}';
    }
}
